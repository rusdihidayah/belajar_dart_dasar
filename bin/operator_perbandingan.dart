void main(List<String> args) {
  // operator perbandingan adalah operator yg hasilnya itu adalah boolean
  // operatoe perbandingan bisa digunakan di tipe daya number dan string
  print(10 == 10); // sama dengan | hasilnya true
  print(10 != 10); // tidak sama dengan | hasilnya false
  print(10 > 10); // besar dari | hasilnya false
  print(10 < 10); // kecil dari | hasilnya false
  print(10 >= 10); // besar dari atau sama dengan | hasilnya true
  print(10 <= 10); // kecil dari atau sama dengan | hasilnya true

  print('Rusdi' == 'Rusdi'); // hasilnya true
  print('Rusdi' != 'Rusdi'); // hasilnya false
}
