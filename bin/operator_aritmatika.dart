void main(List<String> args) {
  // operator aritmatika
  var first = 10;
  var second = 3;

  print(first + second); // penjumlahan
  print(first - second); // pengurangan
  print(first * second); // perkalian
  print(first / second); // pembagian hasil double
  print(first ~/ second); // pembagian hasil integer
  print(first % second); // modulo atau sisa bagi
}
